void test_FFT_BF_I(uint32_t num_of_sections, uint32_t section_size, uint32_t ifft)
{
    uint32_t i, before = 0, after = 0, latency_soft, latency_hard;
    bool mismatch = false;
    
    for (i = 0; i < FFT_N * 2; ++i) {
        fft_sample_buffer_hard[i] = fft_sample_buffer_soft[i];
    }
   
    disable_dsp_arty();
    before = rdmcycle();
    pulserain_FFT_BF_I (fft_sample_buffer_soft, num_of_sections, section_size, ifft);
    after  = rdmcycle();
    
    latency_soft = after - before;
    
    printf (" latency soft = %d\n", latency_soft);
    
    enable_dsp_arty();
    before = rdmcycle();
    pulserain_FFT_BF_I (fft_sample_buffer_hard, num_of_sections, section_size, ifft);
    after  = rdmcycle();
    
    latency_hard = after - before;
    
    printf (" latency hard = %d\n", latency_hard);
        
    for (i = 0; i < FFT_N; ++i) {
                
        if (fft_sample_buffer_soft[i*2] != fft_sample_buffer_hard[i*2]) {
            printf ("mismatch %d, %d vs %d\n", i * 2, fft_sample_buffer_soft[i*2], fft_sample_buffer_hard[i*2]);
            mismatch = true;
        }
        
        if (fft_sample_buffer_soft[i*2+1] != fft_sample_buffer_hard[i*2+1]) {
            printf ("mismatch %d, %d vs %d\n", i * 2 + 1, fft_sample_buffer_soft[i*2 + 1], fft_sample_buffer_hard[i*2 + 1]);
            mismatch = true;
        }
    }
    
    test_assert (!mismatch, "BF I result matches!", "BF I result mismatches!");
    test_improvement (latency_soft, latency_hard); 
        
}


